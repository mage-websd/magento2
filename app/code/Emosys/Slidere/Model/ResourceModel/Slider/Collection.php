namespace Emosys\Slidere\Model\ResourceModel\Slider;

/**
 * Blog post collection
 */
class Collection extends \Magento\Framework\Model\ResourceModel\Db\Collection\AbstractCollection
{
    /**
     * @var \Magento\Store\Model\StoreManagerInterface
     */
    protected $_storeManager;

    /**
     * @var \Magento\Framework\Stdlib\DateTime\DateTime
     */
    protected $_date;

    /**
     * 
     * @param \Magento\Framework\Data\Collection\EntityFactory $entityFactory
     * @param \Psr\Log\LoggerInterface $logger
     * @param \Magento\Framework\Data\Collection\Db\FetchStrategyInterface $fetchStrategy
     * @param \Magento\Framework\Event\ManagerInterface $eventManager
     * @param \Magento\Store\Model\StoreManagerInterface $storeManager
     * @param type $connection
     * @param \Magento\Framework\Model\ResourceModel\Db\AbstractDb $resource
     * @return \Emosys\Slidere\Model\ResourceModel\Slider\Collection
     */
    public function __construct(
        \Magento\Framework\Data\Collection\EntityFactory $entityFactory,
        \Psr\Log\LoggerInterface $logger,
        \Magento\Framework\Data\Collection\Db\FetchStrategyInterface $fetchStrategy,
        \Magento\Framework\Event\ManagerInterface $eventManager,
        \Magento\Store\Model\StoreManagerInterface $storeManager,
        $connection = null,
        \Magento\Framework\Model\ResourceModel\Db\AbstractDb $resource = null
    ) {
        parent::__construct($entityFactory, $logger, $fetchStrategy, $eventManager, $connection, $resource);
        $this->_storeManager = $storeManager;
        return $this;
    }

    /**
     * 
     * @return \Emosys\Slidere\Model\ResourceModel\Slider\Collection
     */
    protected function _construct()
    {
        parent::_construct();
        $this->_init('Emosys\Slidere\Model\Slider', 'Emosys\Slidere\Model\ResourceModel\Slider');
        return $this;
    }

    /**
     * Add is_active filter to collection
     * @return $this
     */
    public function addDateFilter()
    {
        return $this
            ->addFieldToFilter('publish_time', array('lteq' => $this->_date->gmtDate()));
    }

    /**
     * Get SQL for get record count
     *
     * Extra GROUP BY strip added.
     *
     * @return \Magento\Framework\DB\Select
     */
    public function getSelectCountSql()
    {
        $countSelect = parent::getSelectCountSql();
        $countSelect->reset(\Magento\Framework\DB\Select::GROUP);

        return $countSelect;
    }
}
